      Subroutine JH ( a, b, c, e1, e2, x, y, z,J,H ) 
      Double Precision a 
      Double Precision b 
      Double Precision c 
      Double Precision e1 
      Double Precision e2 
      Double Precision x 
      Double Precision y 
      Double Precision z 
      Double Precision J(1,5) 
      Double Precision H(5,5) 
      J(1,1) = -2*(x/a)**(2/e2)*((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1-1)
     1   /(a*e1)
      J(1,2) = -2*(y/b)**(2/e2)*((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1-1)
     1   /(b*e1)
      J(1,3) = -2*(z/c)**(2/e1)/(c*e1)
      J(1,4) = -2*(z/c)**(2/e1)*log(z/c)/e1**2-e2*((y/b)**(2/e2)+(x/a)**
     1   (2/e2))**(e2/e1)*log((y/b)**(2/e2)+(x/a)**(2/e2))/e1**2
      J(1,5) = ((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1)*(log((y/b)**(2/e2)
     1   +(x/a)**(2/e2))/e1+e2*(-2*(y/b)**(2/e2)*log(y/b)/e2**2-2*(x/a)*
     2   *(2/e2)*log(x/a)/e2**2)/(e1*((y/b)**(2/e2)+(x/a)**(2/e2))))
      H(1,1) = [4*(x/a)**(2/e2)*((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1-1)
     1   /(a**2*e1*e2)+2*(x/a)**(2/e2)*((y/b)**(2/e2)+(x/a)**(2/e2))**(e
     2   2/e1-1)/(a**2*e1)+4*(e2/e1-1)*(x/a)**(4/e2)*((y/b)**(2/e2)+(x/a
     3   )**(2/e2))**(e2/e1-2)/(a**2*e1*e2)]
      H(1,2) = [4*(e2/e1-1)*(x/a)**(2/e2)*(y/b)**(2/e2)*((y/b)**(2/e2)+(
     1   x/a)**(2/e2))**(e2/e1-2)/(a*b*e1*e2)]
      H(1,3) = [0]
      H(1,4) = [2*e2*(x/a)**(2/e2)*((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1
     1   -1)*log((y/b)**(2/e2)+(x/a)**(2/e2))/(a*e1**3)+2*(x/a)**(2/e2)*
     2   ((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1-1)/(a*e1**2)]
      H(1,5) = [4*(x/a)**(2/e2)*log(x/a)*((y/b)**(2/e2)+(x/a)**(2/e2))**
     1   (e2/e1-1)/(a*e1*e2**2)-2*(x/a)**(2/e2)*((y/b)**(2/e2)+(x/a)**(2
     2   /e2))**(e2/e1-1)*(log((y/b)**(2/e2)+(x/a)**(2/e2))/e1+(e2/e1-1)
     3   *(-2*(y/b)**(2/e2)*log(y/b)/e2**2-2*(x/a)**(2/e2)*log(x/a)/e2**
     4   2)/((y/b)**(2/e2)+(x/a)**(2/e2)))/(a*e1)]
      H(2,1) = [4*(e2/e1-1)*(x/a)**(2/e2)*(y/b)**(2/e2)*((y/b)**(2/e2)+(
     1   x/a)**(2/e2))**(e2/e1-2)/(a*b*e1*e2)]
      H(2,2) = [4*(y/b)**(2/e2)*((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1-1)
     1   /(b**2*e1*e2)+2*(y/b)**(2/e2)*((y/b)**(2/e2)+(x/a)**(2/e2))**(e
     2   2/e1-1)/(b**2*e1)+4*(e2/e1-1)*(y/b)**(4/e2)*((y/b)**(2/e2)+(x/a
     3   )**(2/e2))**(e2/e1-2)/(b**2*e1*e2)]
      H(2,3) = [0]
      H(2,4) = [2*e2*(y/b)**(2/e2)*((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1
     1   -1)*log((y/b)**(2/e2)+(x/a)**(2/e2))/(b*e1**3)+2*(y/b)**(2/e2)*
     2   ((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1-1)/(b*e1**2)]
      H(2,5) = [4*(y/b)**(2/e2)*((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1-1)
     1   *log(y/b)/(b*e1*e2**2)-2*(y/b)**(2/e2)*((y/b)**(2/e2)+(x/a)**(2
     2   /e2))**(e2/e1-1)*(log((y/b)**(2/e2)+(x/a)**(2/e2))/e1+(e2/e1-1)
     3   *(-2*(y/b)**(2/e2)*log(y/b)/e2**2-2*(x/a)**(2/e2)*log(x/a)/e2**
     4   2)/((y/b)**(2/e2)+(x/a)**(2/e2)))/(b*e1)]
      H(3,1) = [0]
      H(3,2) = [0]
      H(3,3) = [2*(z/c)**(2/e1)/(c**2*e1)+4*(z/c)**(2/e1)/(c**2*e1**2)]
      H(3,4) = [4*(z/c)**(2/e1)*log(z/c)/(c*e1**3)+2*(z/c)**(2/e1)/(c*e1
     1   **2)]
      H(3,5) = [0]
      H(4,1) = [2*e2*(x/a)**(2/e2)*((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1
     1   -1)*log((y/b)**(2/e2)+(x/a)**(2/e2))/(a*e1**3)+2*(x/a)**(2/e2)*
     2   ((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1-1)/(a*e1**2)]
      H(4,2) = [2*e2*(y/b)**(2/e2)*((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1
     1   -1)*log((y/b)**(2/e2)+(x/a)**(2/e2))/(b*e1**3)+2*(y/b)**(2/e2)*
     2   ((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1-1)/(b*e1**2)]
      H(4,3) = [4*(z/c)**(2/e1)*log(z/c)/(c*e1**3)+2*(z/c)**(2/e1)/(c*e1
     1   **2)]
      H(4,4) = [4*(z/c)**(2/e1)*log(z/c)**2/e1**4+4*(z/c)**(2/e1)*log(z/
     1   c)/e1**3+e2**2*((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1)*log((y/b)
     2   **(2/e2)+(x/a)**(2/e2))**2/e1**4+2*e2*((y/b)**(2/e2)+(x/a)**(2/
     3   e2))**(e2/e1)*log((y/b)**(2/e2)+(x/a)**(2/e2))/e1**3]
      H(4,5) = [-e2*((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1)*log((y/b)**(2
     1   /e2)+(x/a)**(2/e2))*(log((y/b)**(2/e2)+(x/a)**(2/e2))/e1+e2*(-2
     2   *(y/b)**(2/e2)*log(y/b)/e2**2-2*(x/a)**(2/e2)*log(x/a)/e2**2)/(
     3   e1*((y/b)**(2/e2)+(x/a)**(2/e2))))/e1**2-((y/b)**(2/e2)+(x/a)**
     4   (2/e2))**(e2/e1)*log((y/b)**(2/e2)+(x/a)**(2/e2))/e1**2-e2*((y/
     5   b)**(2/e2)+(x/a)**(2/e2))**(e2/e1-1)*(-2*(y/b)**(2/e2)*log(y/b)
     6   /e2**2-2*(x/a)**(2/e2)*log(x/a)/e2**2)/e1**2]
      H(5,1) = [((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1)*(2*(x/a)**(2/e2)*
     1   (-2*(y/b)**(2/e2)*log(y/b)/e2**2-2*(x/a)**(2/e2)*log(x/a)/e2**2
     2   )/(a*e1*((y/b)**(2/e2)+(x/a)**(2/e2))**2)+e2*(4*(x/a)**(2/e2)*l
     3   og(x/a)/(a*e2**3)+2*(x/a)**(2/e2)/(a*e2**2))/(e1*((y/b)**(2/e2)
     4   +(x/a)**(2/e2)))-2*(x/a)**(2/e2)/(a*e1*e2*((y/b)**(2/e2)+(x/a)*
     5   *(2/e2))))-2*(x/a)**(2/e2)*((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e
     6   1-1)*(log((y/b)**(2/e2)+(x/a)**(2/e2))/e1+e2*(-2*(y/b)**(2/e2)*
     7   log(y/b)/e2**2-2*(x/a)**(2/e2)*log(x/a)/e2**2)/(e1*((y/b)**(2/e
     8   2)+(x/a)**(2/e2))))/(a*e1)]
      H(5,2) = [((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1)*(2*(y/b)**(2/e2)*
     1   (-2*(y/b)**(2/e2)*log(y/b)/e2**2-2*(x/a)**(2/e2)*log(x/a)/e2**2
     2   )/(b*e1*((y/b)**(2/e2)+(x/a)**(2/e2))**2)+e2*(4*(y/b)**(2/e2)*l
     3   og(y/b)/(b*e2**3)+2*(y/b)**(2/e2)/(b*e2**2))/(e1*((y/b)**(2/e2)
     4   +(x/a)**(2/e2)))-2*(y/b)**(2/e2)/(b*e1*e2*((y/b)**(2/e2)+(x/a)*
     5   *(2/e2))))-2*(y/b)**(2/e2)*((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e
     6   1-1)*(log((y/b)**(2/e2)+(x/a)**(2/e2))/e1+e2*(-2*(y/b)**(2/e2)*
     7   log(y/b)/e2**2-2*(x/a)**(2/e2)*log(x/a)/e2**2)/(e1*((y/b)**(2/e
     8   2)+(x/a)**(2/e2))))/(b*e1)]
      H(5,3) = [0]
      H(5,4) = [((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1)*(-log((y/b)**(2/e
     1   2)+(x/a)**(2/e2))/e1**2-e2*(-2*(y/b)**(2/e2)*log(y/b)/e2**2-2*(
     2   x/a)**(2/e2)*log(x/a)/e2**2)/(e1**2*((y/b)**(2/e2)+(x/a)**(2/e2
     3   ))))-e2*((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1)*log((y/b)**(2/e2
     4   )+(x/a)**(2/e2))*(log((y/b)**(2/e2)+(x/a)**(2/e2))/e1+e2*(-2*(y
     5   /b)**(2/e2)*log(y/b)/e2**2-2*(x/a)**(2/e2)*log(x/a)/e2**2)/(e1*
     6   ((y/b)**(2/e2)+(x/a)**(2/e2))))/e1**2]
      H(5,5) = [((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1)*(log((y/b)**(2/e2
     1   )+(x/a)**(2/e2))/e1+e2*(-2*(y/b)**(2/e2)*log(y/b)/e2**2-2*(x/a)
     2   **(2/e2)*log(x/a)/e2**2)/(e1*((y/b)**(2/e2)+(x/a)**(2/e2))))**2
     3   +((y/b)**(2/e2)+(x/a)**(2/e2))**(e2/e1)*(-e2*(-2*(y/b)**(2/e2)*
     4   log(y/b)/e2**2-2*(x/a)**(2/e2)*log(x/a)/e2**2)**2/(e1*((y/b)**(
     5   2/e2)+(x/a)**(2/e2))**2)+e2*(4*(y/b)**(2/e2)*log(y/b)**2/e2**4+
     6   4*(y/b)**(2/e2)*log(y/b)/e2**3+4*(x/a)**(2/e2)*log(x/a)**2/e2**
     7   4+4*(x/a)**(2/e2)*log(x/a)/e2**3)/(e1*((y/b)**(2/e2)+(x/a)**(2/
     8   e2)))+2*(-2*(y/b)**(2/e2)*log(y/b)/e2**2-2*(x/a)**(2/e2)*log(x/
     9   a)/e2**2)/(e1*((y/b)**(2/e2)+(x/a)**(2/e2))))]
      Return 
      End 
